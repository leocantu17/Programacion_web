let numero = 7;
// Declara una variable llamada 'numero' y le asigna el valor 7

if (numero % 2 === 0) {
  // Verifica si el número es divisible por 2 (es decir, si es par)
  console.log("El número es par");
  // Si la condición es verdadera, imprime "El número es par"
} else {
  console.log("El número es impar");
  // Si la condición es falsa (el número no es divisible por 2), imprime "El número es impar"
}

for (let i = 0; i < 5; i++) {
  // Bucle for que se ejecuta 5 veces (i toma los valores 0, 1, 2, 3 y 4)
  console.log("Iteración:", i);
  // En cada iteración, imprime el número actual de la variable 'i'
}
